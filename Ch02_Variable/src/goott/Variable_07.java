package goott;
/*
 * boolean 자료형
 * false
 * true
 * python은 True, False인데 
 * java는 true, false로 소문자임
*/
public class Variable_07 {

	public static void main(String[] args) {
		boolean flag = false;
		System.out.println( flag ? "ok" : "no");
		flag = true;
		System.out.println( flag ? "ok" : "no");
	}
}